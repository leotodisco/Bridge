package com.project.bridgebackend.Model.Entity.enumeration;

/**
 * @author Mario Zurolo.
 * Creato il 03/12/2024.
 * Questa è l'enumeratore relativa ai servizi,
 * che possono essere disponibili in un alloggio.
 */
public enum Servizi {
    /**
     * Disponibilità di connessione alla rete.
     */
    WIFI,

    /**
     * Disponibilità di riscaldamento in alloggio.
     */
    RISCALDAMENTO,

    /**
     * Disponibilità di aria condizionata in alloggio.
     */
    ARIACONDIZIONATA
}
