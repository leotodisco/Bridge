package com.project.bridgebackend.Model.Entity.enumeration;

/**
 * Enumerazione che rappresenta i tipi di consulenza disponibili.
 */
public enum TipoConsulenza {

    /**
     * Consulenza di tipo sanitario, ad esempio medici o infermieri.
     */
    SANITARIA,

    /**
     * Consulenza di tipo legale, come avvocati o consulenti giuridici.
     */
    LEGALE,

    /**
     * Consulenza di tipo commerciale, per affari e business.
     */
    COMMERCIALE,

    /**
     * Consulenza psicologica, ad esempio supporto terapeutico.
     */
    PSICOLOGICA,

    /**
     * Servizi di traduzione, interpreti o mediatori culturali.
     */
    TRADUTTORE
}
